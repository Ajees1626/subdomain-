export function inferContentType(label) {
  const t = String(label).toLowerCase()
  const hasR = /\breel/.test(t)
  const hasP = /poster/.test(t)
  if (hasR && hasP) return 'mixed'
  if (hasR) return 'reel'
  if (hasP) return 'poster'
  return 'poster'
}

export function buildUploadsForClientColumn(clientIndex, clientId) {
  const uploads = []
  for (let d = 1; d <= 30; d++) {
    const row = APRIL_2026_MATRIX[d]
    if (!row) continue
    const cell = (row[clientIndex] || '').trim()
    if (!cell) continue
    const date = `2026-04-${String(d).padStart(2, '0')}`
    uploads.push({
      id: `${clientId}:${date}`,
      date,
      type: inferContentType(cell),
      title: cell,
    })
  }
  return uploads
}

export function nextScheduledFromUploads(uploads) {
  const sorted = [...uploads].sort((a, b) => a.date.localeCompare(b.date))
  const last = sorted[sorted.length - 1]
  if (!last) return { date: '2026-05-01', type: 'poster', title: 'Schedule May' }
  return { date: '2026-05-01', type: 'poster', title: 'Continue after Apr 30' }
}
